/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import types from 'configs/types'

/*******************************************************************************************************/
// Interface de Action //
/*******************************************************************************************************/
interface IAction {
  type: string
  payload: any
}

/*******************************************************************************************************/
// Interface del Reducer //
/*******************************************************************************************************/
export interface IChatPanelReducer {
  state: boolean
  contacts: any
  chat: any
}

/*******************************************************************************************************/
// Estado inicial del Reducer //
/*******************************************************************************************************/
const initialState: IChatPanelReducer = {
  state: false,
  contacts: {},
  chat: {}
}

/*******************************************************************************************************/
// Definimos el reducer y sus mÃ©todos //
/*******************************************************************************************************/
const chatPanelReducer = (state = initialState, { type, payload }: IAction) => {
  switch (type) {
    case types.toggleChatPanel:
      return {
        ...state,
        state: !state.state
      }
    case types.openChatPanel:
      return {
        ...state,
        state: true
      }
    case types.closeChatPanel:
      return {
        ...state,
        state: false
      }
    default:
      return state
  }
}

/*******************************************************************************************************/
// Exportamos el reducer //
/*******************************************************************************************************/
export default chatPanelReducer
