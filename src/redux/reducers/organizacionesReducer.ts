/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import types from 'configs/types'

/*******************************************************************************************************/
// Interface de Action //
/*******************************************************************************************************/
interface IAction {
  type: string
  payload: any
}

/*******************************************************************************************************/
// Interface del Reducer //
/*******************************************************************************************************/
export interface IOrganizacionesReducer {
  nombre: string
  change: string
}

/*******************************************************************************************************/
// Estado inicial del Reducer //
/*******************************************************************************************************/
const initialState: IOrganizacionesReducer = {
  nombre: '',
  change: ''
}

/*******************************************************************************************************/
// Definimos el reducer y sus mÃ©todos //
/*******************************************************************************************************/
const organizacionesReducer = (state = initialState, { type, payload }: IAction) => {
  switch (type) {
    case types.setOrganizacionesNombre:
      return {
        ...state,
        nombre: payload
      }
    case types.resetOrganizaciones:
      return { ...initialState, change: `${new Date()}` }
    default:
      return state
  }
}

/*******************************************************************************************************/
// Exportamos el reducer //
/*******************************************************************************************************/
export default organizacionesReducer
