/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import types from 'configs/types';
import { Dispatch } from 'redux';

/*******************************************************************************************************/
// Función para el evento Iniciar Alternar el Panel Rápido //
/*******************************************************************************************************/
export const startToggleQuickPanel = () => {
	return (dispatch: Dispatch) => {
		dispatch(toggleQuickPanel());
	};
};

/*******************************************************************************************************/
// Accion para el evento Alternar el Panel Rápido //
/*******************************************************************************************************/
export const toggleQuickPanel = () => {
	return {
		type: types.toggleQuickPanel
	};
};

/*******************************************************************************************************/
// Accion para el evento Abrir el Panel Rápido //
/*******************************************************************************************************/
export const openQuickPanel = () => {
	return {
		type: types.openQuickPanel
	};
};

/*******************************************************************************************************/
// Accion para el evento Cerrar el Panel Rápido //
/*******************************************************************************************************/
export const closeQuickPanel = () => {
	return {
		type: types.closeQuickPanel
	};
};
